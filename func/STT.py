VIDEO_FILE = "../data/p13_gross_motor_1.mp4"  # 원본 비디오 파일이 저장된 경로를 지정합니다.
OUTPUT_FOLDER = "../data/p13_gross_motor_1_ass"  # 자막 파일들이 저장될 폴더 경로를 지정합니다.

# 기존 model_size="base"를 고정밀 "turbo" 모델로 변경하여 함수를 호출합니다.
